package login;

public class SizeDTO {
	String id;
	double top_length;
	double shoulder_width;
	double chest;
	double arm_length;
	double arm_circle;
	double waist_circle;
	double thigh_circle;
	double leg_length;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public double getTop_length() {
		return top_length;
	}
	public void setTop_length(double top_length) {
		this.top_length = top_length;
	}
	public double getShoulder_width() {
		return shoulder_width;
	}
	public void setShoulder_width(double shoulder_width) {
		this.shoulder_width = shoulder_width;
	}
	public double getChest() {
		return chest;
	}
	public void setChest(double chest) {
		this.chest = chest;
	}
	public double getArm_length() {
		return arm_length;
	}
	public void setArm_length(double arm_length) {
		this.arm_length = arm_length;
	}
	public double getArm_circle() {
		return arm_circle;
	}
	public void setArm_circle(double arm_circle) {
		this.arm_circle = arm_circle;
	}
	public double getWaist_circle() {
		return waist_circle;
	}
	public void setWaist_circle(double waist_circle) {
		this.waist_circle = waist_circle;
	}
	public double getThigh_circle() {
		return thigh_circle;
	}
	public void setThigh_circle(double thigh_circle) {
		this.thigh_circle = thigh_circle;
	}
	public double getLeg_length() {
		return leg_length;
	}
	public void setLeg_length(double leg_length) {
		this.leg_length = leg_length;
	}
	
}
